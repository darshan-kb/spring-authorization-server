    public String forgotPassword(String email){
        User user = userRepository.findByEmail(email).orElseThrow(()-> new UsernameNotFoundException("User not found"));
        String token = signUpService.createTokenAndSave(user);
        String link ="http://localhost:8080/api/account/forgotpassword/changepassword?token=" + token;
        emailSender.send(email,buildEmail(user.getUsername(),link),"Confirm your email for forgot password");
        return "Please click on the link send on your email";
    }

    public String changePassword(String token, String password){
        ConfirmationToken confirmationToken = confirmationTokenRepository.findByToken(token).orElseThrow(()-> new IllegalStateException("Invalid token"));
        if(confirmationToken.getConfirmedAt()!=null){
            throw new IllegalStateException("Password has been changed");
        }

        LocalDateTime expiredAt = confirmationToken.getExpiresAt();

        if(expiredAt.isBefore(LocalDateTime.now())){
            throw new IllegalStateException("token expired");
        }


        confirmationTokenService.setConfirmedAt(token);
        String email = confirmationToken.getUser().getEmail();
        User user = confirmationToken.getUser();
        user.setPassword(bCryptPasswordEncoder.encode(password));
        userRepository.save(user);
        return "Password Changed!";
    }


